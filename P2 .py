# This program will print out a message dependent on whether the number is even or odd.

numbers = [85,77,10,1,86,88,48,93,59,60]

for number in numbers:
    if number % 2==0:
        print (f"Hey, {number} is even!")
    else:
        print (f"Gosh, {number} is odd!")